class Gagyi{
	public static void main (String[] args){
            if (args.length != 1){
                System.out.println("Használat: java Gagyi szám");
                return;
            }
            
            Integer x = Integer.parseInt(args[0]);
            Integer t = Integer.parseInt(args[0]);
                
            while (x >= t && x <= t && x != t){
                System.out.println("Gagyiiii");
            }
	}
} 
